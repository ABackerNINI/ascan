void mfile::output_mk_build_if_option_b() {
    // Character before command:
    //   '@': turn off echo.
    //   '-': ignore error, make will exit when error occurs.
    //   '+': ignore make's -n -t -q options.
    if (m_flags & OPTION_B) {
        // OUT @mkdir -p "$(BUILD)"
        m_fout << "\t@mkdir -p \"$(" << CONFIG_BD << ")\"" << endl;
    }
}

